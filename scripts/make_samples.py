"""生成演示素材：3 份历史技术方案 + 1 份招标文件（docx）。幂等，重跑覆盖。"""
from pathlib import Path

from docx import Document

OUT = Path("samples")

# ---------- 历史方案共用章节模板（术语三份保持一致） ----------

def _proposal(title: str, scene: str, extras: dict) -> None:
    """生成一份历史技术方案 docx。extras: 章节名 -> 补充段落。"""
    doc = Document()
    doc.add_heading(title, level=0)
    chapters = {
        "总体架构设计": (
            f"本项目{scene}采用云原生微服务架构，整体分为基础设施层、数据资源层、平台服务层与应用呈现层四层。"
            "基础设施层基于国产化虚拟化平台构建资源池，计算节点按 1:4 超配比规划，支持横向弹性扩容。"
            "平台服务层以容器化方式部署综合管理平台各微服务，通过服务网格实现服务注册、发现与熔断治理，"
            "平台整体可用性设计目标不低于 99.9%。"
        ),
        "网络与安防设计": (
            "园区网络采用核心-汇聚-接入三层组网，核心交换机双机热备，万兆骨干、千兆到桌面。"
            "视频监控系统采用视频智能分析系统统一接入，支持不少于 2000 路高清视频并发接入，"
            "关键出入口部署人脸识别闸机与车牌识别一体机，事件告警联动时间不超过 3 秒。"
        ),
        "物联网感知体系": (
            "构建统一物联网接入平台，支持 MQTT、CoAP、Modbus 等多协议接入，单节点支持 10 万级设备并发连接。"
            "部署环境监测、智能照明、能耗采集、井盖水位等各类感知终端，数据经边缘网关汇聚后统一上报数据中台。"
        ),
        "数据中台建设": (
            "数据中台包含数据汇聚、数据治理、数据资产与数据服务四个能力域。"
            "通过 ETL 与实时流两条链路汇聚业务数据，建立统一的数据标准与主数据管理体系，"
            "对外以 API 网关方式提供数据服务，日均支撑数据服务调用不低于 500 万次。"
        ),
        "应用系统建设": (
            "面向管理方、企业与公众三类角色建设综合管理平台门户、移动端 App 与运营驾驶舱。"
            "运营驾驶舱基于数据中台提供园区运行一屏总览，涵盖安防态势、能耗分析、设备健康度与企业服务热力图。"
        ),
        "安全保障体系": (
            "按照等级保护三级要求设计安全体系，覆盖物理安全、网络安全、主机安全、应用安全与数据安全五个层面。"
            "核心数据库启用透明加密与脱敏，重要操作全程审计，安全事件统一接入态势感知平台。"
        ),
        "项目实施方案": (
            "项目实施划分为需求调研、深化设计、开发实施、联调测试、试运行与验收六个阶段，"
            "采用双周迭代交付，关键里程碑设置阶段评审。现场实施团队不少于 12 人，其中高级工程师占比 40% 以上。"
        ),
        "运维保障方案": (
            "提供 7×24 小时运维服务，建立三级响应机制：一般故障 4 小时内解决，重大故障 30 分钟内到场。"
            "部署统一运维监控平台，对主机、网络、应用与数据库全栈监控，月度出具运行分析报告。"
        ),
        "培训与交付": (
            "分管理层、业务操作层与运维技术层三类对象开展培训，累计不少于 40 课时，"
            "交付物包括系统设计文档、操作手册、运维手册与源代码，并提供为期一年的免费质保服务。"
        ),
    }
    for i, (name, body) in enumerate(chapters.items(), 1):
        doc.add_heading(f"第{i}章 {name}", level=1)
        doc.add_paragraph(body)
        if name in extras:
            doc.add_paragraph(extras[name])
    OUT.mkdir(exist_ok=True)
    doc.save(str(OUT / f"历史方案-{title}.docx"))

def make_proposals() -> None:
    _proposal("智慧园区综合管理平台", "智慧园区", {
        "应用系统建设": "本项目还建设了访客预约、会议室预订、智慧停车与一码通行应用，"
                    "一码通行覆盖门禁、停车、消费与访客四大场景，上线后园区通行效率提升 60%。",
    })
    _proposal("政务数据中台", "政务信息化", {
        "数据中台建设": "本项目汇聚 42 个委办局共 1800 余类数据资源，建成人口、法人、电子证照三大基础库，"
                    "支撑一网通办事项 320 项，数据共享交换日均 200 万条。",
    })
    _proposal("园区安防系统", "园区安防", {
        "网络与安防设计": "本项目部署高清摄像机 1500 路，视频智能分析系统支持人员聚集、越界入侵、"
                     "烟火识别等 15 类算法，告警准确率不低于 92%，并与消防、门禁系统实现联动。",
    })

# ---------- 招标文件 ----------

def make_tender() -> None:
    doc = Document()
    doc.add_heading("某开发区智慧园区综合管理平台建设项目招标文件", level=0)

    doc.add_heading("第一章 项目概况", level=1)
    doc.add_paragraph(
        "某开发区管理委员会拟建设智慧园区综合管理平台，实现园区安防、物联、数据与应用服务的统一管理。"
        "项目预算金额为人民币 1200 万元，建设工期为合同签订后 180 日历天，"
        "其中平台上线试运行须在合同签订后 150 日历天内完成，试运行期 30 天。"
        "关键里程碑：第 30 天完成深化设计评审，第 90 天完成平台核心功能开发，第 150 天上线试运行。"
    )

    doc.add_heading("第二章 技术需求", level=1)
    reqs = [
        ("总体架构", "平台须采用云原生微服务架构，支持容器化部署与横向弹性扩容"),
        ("平台可用性", "平台整体可用性不低于 99.9%，核心服务支持双机热备"),
        ("视频接入", "视频监控系统支持不少于 2000 路高清视频并发接入"),
        ("视频智能分析", "支持人员聚集、越界入侵、烟火识别等不少于 12 类智能分析算法，告警准确率不低于 90%"),
        ("告警联动", "安防事件告警联动响应时间不超过 3 秒"),
        ("物联接入", "物联网平台支持 MQTT、CoAP、Modbus 等主流协议，单节点支持 10 万级设备并发连接"),
        ("数据中台", "建设数据汇聚、治理、资产与服务能力，对外提供统一数据服务 API"),
        ("数据服务能力", "数据服务日均调用支撑能力不低于 500 万次"),
        ("一码通行", "实现门禁、停车、访客一码通行，支持与既有门禁系统对接"),
        ("运营驾驶舱", "提供园区运行一屏总览，涵盖安防态势、能耗分析与设备健康度"),
        ("移动端", "提供 iOS/Android 移动端应用，支持消息推送与移动审批"),
        ("等级保护", "系统按等级保护三级标准建设并通过测评"),
        ("数据安全", "核心数据库须启用透明加密，敏感数据展示须脱敏"),
        ("国产化适配", "平台须完成与国产化操作系统、数据库的兼容性适配"),
        ("性能要求", "门户页面平均响应时间不超过 2 秒，并发在线用户不少于 5000 人"),
        ("系统对接", "须与管委会既有 OA 系统、一网通办平台完成数据对接"),
    ]
    t = doc.add_table(rows=1, cols=2)
    t.style = "Table Grid"
    t.rows[0].cells[0].text = "需求项"
    t.rows[0].cells[1].text = "技术要求"
    for item, req in reqs:
        row = t.add_row()
        row.cells[0].text = item
        row.cells[1].text = req

    doc.add_heading("第三章 投标人资质要求", level=1)
    for q in [
        "投标人须具有独立法人资格，注册资本不低于 1000 万元；",
        "投标人须具备软件企业认定证书，并通过 CMMI3 级及以上认证；",
        "投标人近三年须具有不少于 2 个类似智慧园区或智慧城市项目业绩，单项合同金额不低于 500 万元；",
        "拟派项目经理须具备信息系统项目管理师（高级）资格，团队核心成员不少于 8 人。",
    ]:
        doc.add_paragraph(q, style="List Bullet")

    doc.add_heading("第四章 评标办法", level=1)
    doc.add_paragraph("本项目采用综合评分法，总分 100 分，其中技术部分 60 分、商务部分 25 分、价格部分 15 分。")
    s = doc.add_table(rows=1, cols=3)
    s.style = "Table Grid"
    for i, h in enumerate(["评分项", "分值", "评分标准"]):
        s.rows[0].cells[i].text = h
    for item, weight, note in [
        ("总体技术方案", "15", "架构先进性、完整性与针对性"),
        ("视频与物联方案", "12", "视频智能分析与物联接入方案的成熟度"),
        ("数据中台方案", "10", "数据治理与数据服务能力设计"),
        ("安全与国产化方案", "8", "等保三级落实与国产化适配方案"),
        ("实施与运维方案", "10", "实施组织、进度保障与运维响应"),
        ("演示与答辩", "5", "系统演示效果与答辩情况"),
        ("企业业绩与资信", "15", "类似业绩、认证与资信"),
        ("本地化服务", "10", "本地化服务团队与响应能力"),
        ("投标报价", "15", "满足要求的最低价为基准价"),
    ]:
        row = s.add_row()
        row.cells[0].text = item
        row.cells[1].text = weight
        row.cells[2].text = note

    doc.add_paragraph(
        "特别说明：投标人应充分考虑与园区既有系统的平滑衔接，优先考虑具备本地化服务团队"
        "并能提供 7×24 小时现场响应的投标人；平台须为后续二期扩展预留接口，"
        "投标人应在方案中说明与既有视频监控设备品牌的兼容适配经验。"
    )
    OUT.mkdir(exist_ok=True)
    doc.save(str(OUT / "招标文件-某开发区智慧园区平台项目.docx"))

if __name__ == "__main__":
    make_proposals()
    make_tender()
    print("samples generated:", [p.name for p in sorted(OUT.glob("*.docx"))])
